from openupgradelib import openupgrade


@openupgrade.migrate()
def migrate(env, version):
    cr = env.cr
    cr.execute("""
    ALTER TABLE res_partner
    RENAME COLUMN bus_passenger TO bus_passenger_tmp;
    """)
